int		ft_sqrt(int nb)
{
	long long i;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 2;
	while (nb >= i * i)
	{
		if (i * i == nb)
			return ((int)i);
		i++;
	}
	return (0);
}
