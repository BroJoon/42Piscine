char	*ft_strupcase(char *str)
{
	int		i;
	char	c;

	i = 0;
	c = 'a' - 'A';
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= c;
		i++;
	}
	return (str);
}
