#include <unistd.h>

void	dfs(int nb)
{
	char c;

	c = nb % 10 + '0';
	if (nb > 9)
	{
		nb = nb / 10;
		dfs(nb);
	}
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb == 0)
	{
		write(1, "0", 1);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = nb * -1;
	}
	dfs(nb);
}
